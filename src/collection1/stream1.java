package collection1;

public class stream1 {
	public String name;
	public int age;
	public int id;
	public String address;
	public stream1(String name, int age, int id, String address) {
		super();
		this.name = name;
		this.age = age;
		this.id = id;
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	 
	

}
